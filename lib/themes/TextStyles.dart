import 'package:flutter/material.dart';

class TextStyles{
    static TextStyle mont_bold({double? fontSize, Color? color}) => TextStyle(fontSize: fontSize, color: color, fontFamily: 'mont_bold');

    static TextStyle mont_regular({double? fontSize, Color? color}) => TextStyle(fontSize: fontSize, color: color, fontFamily: 'mont_regular');

    static TextStyle mont_heavy({double? fontSize, Color? color}) => TextStyle(fontSize: fontSize, color: color, fontFamily: 'mont_heavy');

    static TextStyle mont_semibold({double? fontSize, Color? color}) => TextStyle(fontSize: fontSize, color: color, fontFamily: 'mont_semibold');

    static TextStyle mont_light({double? fontSize, Color? color}) => TextStyle(fontSize: fontSize, color: color, fontFamily: 'mont_light');


}